class Product {
  constructor(plink, ptitle, pstar, pprice, pavailability, color, size) {
    this.pLink = plink;
    this.pTitle = ptitle;
    this.pStar = pstar;
    this.pPrice = pprice;
    this.pAvailability = pavailability;
    this.pColor = color;
    this.pSize = size;
  }
}

module.exports = Product;
